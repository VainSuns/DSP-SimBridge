%% ---- DSP serialization helpers ----
function L = gen_dsp_helpers(used_types)
    L = {};
    need_int16  = any(strcmp(used_types, 'int16'));
    need_uint16 = any(strcmp(used_types, 'uint16'));
    need_int32  = any(strcmp(used_types, 'int32'));
    need_uint32 = any(strcmp(used_types, 'uint32'));
    need_single = any(strcmp(used_types, 'single'));

    % uint16 (DSP: read_int16 does NOT call read_uint16, so only needed for uint16 vars)
    L = [L, gen_block('read_uint16', need_uint16, { ...
        'static inline uint16_t read_uint16(const uint16_t* buf, uint32_t* offset)', ...
        '{', ...
        '    return buf[(*offset)++];', ...
        '}'; ...
    })];
    L = [L, gen_block('write_uint16', need_uint16, { ...
        'static inline void write_uint16(uint16_t* buf, uint32_t* offset, uint16_t val)', ...
        '{', ...
        '    buf[(*offset)++] = val;', ...
        '}'; ...
    })];
    if need_uint16, L{end+1} = ''; end

    % int16
    L = [L, gen_block('read_int16', need_int16, { ...
        'static inline int16_t read_int16(const uint16_t* buf, uint32_t* offset)', ...
        '{', ...
        '    return (int16_t)buf[(*offset)++];', ...
        '}'; ...
    })];
    L = [L, gen_block('write_int16', need_int16, { ...
        'static inline void write_int16(uint16_t* buf, uint32_t* offset, int16_t val)', ...
        '{', ...
        '    buf[(*offset)++] = (uint16_t)val;', ...
        '}'; ...
    })];
    if need_int16, L{end+1} = ''; end

    % uint32 is base for int32/uint32/single and step_index (always needed)
    L = [L, gen_block('read_uint32', true, { ...
        'static inline uint32_t read_uint32(const uint16_t* buf, uint32_t* offset)', ...
        '{', ...
        '    uint32_t lo = (uint32_t)buf[(*offset)++];', ...
        '    uint32_t hi = (uint32_t)buf[(*offset)++];', ...
        '    return lo | (hi << 16);', ...
        '}'; ...
    })];
    L = [L, gen_block('write_uint32', true, { ...
        'static inline void write_uint32(uint16_t* buf, uint32_t* offset, uint32_t val)', ...
        '{', ...
        '    buf[(*offset)++] = (uint16_t)(val & 0xFFFFu);', ...
        '    buf[(*offset)++] = (uint16_t)((val >> 16) & 0xFFFFu);', ...
        '}'; ...
    })];
    L{end+1} = '';

    % int32
    L = [L, gen_block('read_int32', need_int32, { ...
        'static inline int32_t read_int32(const uint16_t* buf, uint32_t* offset)', ...
        '{', ...
        '    return (int32_t)read_uint32(buf, offset);', ...
        '}'; ...
    })];
    L = [L, gen_block('write_int32', need_int32, { ...
        'static inline void write_int32(uint16_t* buf, uint32_t* offset, int32_t val)', ...
        '{', ...
        '    write_uint32(buf, offset, (uint32_t)val);', ...
        '}'; ...
    })];
    if need_int32, L{end+1} = ''; end

    % single
    L = [L, gen_block('read_single', need_single, { ...
        'static inline float read_single(const uint16_t* buf, uint32_t* offset)', ...
        '{', ...
        '    uint32_t raw = read_uint32(buf, offset);', ...
        '    float val;', ...
        '    memcpy(&val, &raw, sizeof(val));', ...
        '    return val;', ...
        '}'; ...
    })];
    L = [L, gen_block('write_single', need_single, { ...
        'static inline void write_single(uint16_t* buf, uint32_t* offset, float val)', ...
        '{', ...
        '    uint32_t raw;', ...
        '    memcpy(&raw, &val, sizeof(raw));', ...
        '    write_uint32(buf, offset, raw);', ...
        '}'; ...
    })];
end

function L = gen_block(~, active, lines)
    L = {};
    if ~active
        for i = 1:numel(lines)
            L{end+1} = ['// ' lines{i}];
        end
    else
        for i = 1:numel(lines)
            L{end+1} = lines{i};
        end
    end
end

