%% ---- Helper functions ----
function bytes = compute_data_bytes(vars)
    bytes = 0;
    for i = 1:numel(vars)
        bytes = bytes + type_wire_bytes(vars(i).type) * vars(i).dim;
    end
end

function b = type_wire_bytes(t)
    switch t
        case {'int16','uint16'}, b = 2;
        case {'int32','uint32','single'}, b = 4;
        case 'double', b = 8;
    end
end

function w = type_word_size(t)
    w = type_wire_bytes(t) / 2;
end

function s = describe_vars(vars)
    parts = cell(1, numel(vars));
    for i = 1:numel(vars)
        v = vars(i);
        if v.dim == 1
            parts{i} = sprintf('%d x %s', type_wire_bytes(v.type)/2, v.type);
        else
            parts{i} = sprintf('%d x %s[%d]', v.dim, v.type, v.dim);
        end
    end
    s = strjoin(parts, ' + ');
end

function c = type_to_c_type(t)
    switch t
        case 'int16',  c = 'int16_t';
        case 'uint16', c = 'uint16_t';
        case 'int32',  c = 'int32_t';
        case 'uint32', c = 'uint32_t';
        case 'single', c = 'float';
        case 'double', c = 'double';
    end
end

